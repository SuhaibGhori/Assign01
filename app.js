// Populate brand dropdown
let brand = document.getElementById("brand");
let model = document.getElementById("model");
let brandKeys = Object.keys(mobiles);

brandKeys.forEach((brandName) => {
    brand.innerHTML += `<option value="${brandName}">${brandName.charAt(0).toUpperCase() + brandName.slice(1)}</option>`;
});

function selectBrand() {
    let selectedBrand = brand.value;
    model.innerHTML = `<option value="">Select Model</option>`; // Reset model dropdown
    if (selectedBrand && mobiles[selectedBrand]) {
        let allModels = Object.keys(mobiles[selectedBrand]);
        allModels.forEach((modelName) => {
            model.innerHTML += `<option value="${modelName}">${modelName}</option>`;
        });
    }
}

function searchbtn() {
  var brand = document.getElementById("brand").value;
  var model = document.getElementById("model").value;
  
  if (brand && model && mobiles[brand] && mobiles[brand][model]) {
      var mobile = mobiles[brand][model];
      var cardPortion = document.getElementById("cardPortion");
      cardPortion.innerHTML = ''; // Clear previous cards
      
      // Ensure 'specialFeatures' is an array and use 'join' safely
      var specialFeatures = Array.isArray(mobile.specialFeatures) ? mobile.specialFeatures.join(", ") : "No special features available";
      
      var cardHTML = `
          <div class="card" style="width: 18rem;">
              <img src="${mobile.image}" class="card-img-top" alt="${mobile.model}">
              <div class="card-body">
                  <h5 class="card-title">${model}</h5>
                  <p class="card-text">Processor: ${mobile.processor}</p>
                  <p class="card-text">RAM: ${mobile.memory.ram}GB</p>
                  <p class="card-text">Storage: ${mobile.memory.storage}GB</p>
                  <p class="card-text">Battery: ${mobile.battery}mAh</p>
                  <p class="card-text">Operating System: ${mobile.operatingSystem}</p>
                  <p class="card-text">Special Features: ${specialFeatures}</p>
              </div>
          </div>
      `;
      
      cardPortion.innerHTML = cardHTML;
  } else {
      alert("Please select a valid brand and model.");
  }
}